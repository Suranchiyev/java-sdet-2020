package day3;

public class WholeNumbers {
	public static void main(String args[]) {
		int age = 30;
		System.out.println(age);

		age = 25;
		System.out.println(age);

		age = age + 10;
		System.out.println(age);

		byte num = 120;
		System.out.println(num);

		short num1 = 12321;
		System.out.println(num1);

		long bigNumber = 300000000000L;
		System.out.println(bigNumber);
	}
}